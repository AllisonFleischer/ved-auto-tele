t.version = "1.0"
t.minimumved = "b14"
t.description = [[
Generate scripts to teleport the player!
When naming a script, use this format:

tx,y\y

The plugin will then generate a load script by that
name and an internal script that will teleport
the player to those coordinates.

Any non-digit character can be used as a delimiter.
Example:

t4-16\y

Note that coordinates start at 1 like in the editor,
not at 0 like in internal scripting.
]]
